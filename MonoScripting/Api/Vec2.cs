public struct Vec2
   {
    public Vec2(float x, float y)
    {
        this.x = x;
        this.y = y;
    }

    public float x;
    public float y;
}

public struct Vec2i
   {
    public Vec2i(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int x;
    public int y;
}